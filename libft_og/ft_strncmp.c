#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (*s1 && *s2)
	{
		return (s1[n - 1] - s2[n - 1]);
		s1++;
		s2++;
	}
	return (s1[0] - s2[0]);
}
